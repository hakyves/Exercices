package exercise1;

public class SwitchCase {
    public static void main(String []args){
        int number = 7;
        switch(number){
            case 1 :
                System.out.println("The number is 1");
                break;
            case 2:
                System.out.println("The number is 2");
                break;
            case 3:
                    System.out.println("The number is 3");

            default:
                System.out.println("No choices");

        }
    }
}
